#!/bin/bash
set -euo pipefail

# =============================================================================
# 03_deploy_services.sh - 서비스 배포 스크립트
# 카테고리: 배포
# 설명: Docker Compose를 사용하여 전체 서비스 배포
# =============================================================================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(dirname "$(dirname "$SCRIPT_DIR")")"

echo "=========================================="
echo "서비스 배포 시작"
echo "=========================================="

cd "$PROJECT_ROOT"

# 환경 변수 파일 확인
if [ ! -f ".env" ]; then
    echo "오류: .env 파일이 존재하지 않습니다."
    echo "  .env.example을 복사하여 .env 파일을 생성해주세요."
    exit 1
fi

# 서비스 시작
echo "[1/3] 컨테이너 시작..."
docker-compose up -d

# 서비스 상태 확인
echo "[2/3] 서비스 시작 대기 (30초)..."
sleep 30

echo "[3/3] 서비스 상태 확인..."
docker-compose ps

echo "=========================================="
echo "서비스 배포 완료"
echo "=========================================="
